    private void tableUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUserMouseClicked
        // TODO add your handling code here:
       int baris = tableUser.rowAtPoint(evt.getPoint());
       
       String userName = tableUser.getValueAt(baris, 0).toString();
       String email = tableUser.getValueAt(baris, 1).toString();
       String fullName = tableUser.getValueAt(baris, 2).toString();
       String status = tableUser.getValueAt(baris, 3).toString();
       
       tUser.setText(userName);
       tEmail.setText(email);
       tFullName.setText(fullName);
       cbStatus.setSelectedItem(status);
        
    }//GEN-LAST:event_tableUserMouseClicked

