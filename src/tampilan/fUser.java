    void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("user Name");
        model.addColumn("user Email");
        model.addColumn("user Full Name");
        model.addColumn("user Status");
        
        try {
            //membuat objek user dan mengambil data dari database
            user us = new user();
            ResultSet result = us.tampilUser();
            //loop data perbaris
            while (result.next()) {
                //cek status : 1 active, selain itu = inactive
                String status = (result.getInt("userStatus")== 1) ? "Active" : "Inactive";
                //tambahkan baris kedalam table model 
                model.addRow(new Object[]{
                result.getString("userName"),
                result.getString("userEmail"),
                result.getString("userFullName"),
                status
                });   
            }
            //set model ke Jtable
            tableUser.setModel(model);
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
        
        

    }

