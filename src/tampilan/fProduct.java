    private void tableProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductMouseClicked
        // TODO add your handling code here:
        int baris = tableProduct.rowAtPoint(evt.getPoint());
        
        int id = Integer.parseInt(tableProduct.getValueAt(baris, 0).toString());
        String nama = tableProduct.getValueAt(baris, 1).toString();
        String category = tableProduct.getValueAt(baris, 2).toString();
        String desc = tableProduct.getValueAt(baris, 3).toString();
        int price = Integer.parseInt(tableProduct.getValueAt(baris, 4).toString());
        
        tId.setText(String.valueOf(id));
        tNama.setText(nama);
        tCategory.setText(category);
        tDesc.setText(desc);
        tprice.setText(String.valueOf(price));
  
    }//GEN-LAST:event_tableProductMouseClicked

