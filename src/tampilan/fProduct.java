    void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Product Id");
        model.addColumn("Product Name");
        model.addColumn("Product Category");
        model.addColumn("Product Description");
        model.addColumn("Product Price");
        
        
           try {
            //membuat objek user dan mengambil data dari database
            product pro = new product();
            ResultSet result = pro.tampilProduct();
            
            while (result.next()) {
            //tambahkan baris kedalam table model 
            model.addRow(new Object[]{
                result.getString("productId"),
                result.getString("ProductName"),
                result.getString("categoryId"),
                result.getString("productDescription"),
                result.getString("productPrice")
           
            });
            }
            //set model ke Jtable
            tableProduct.setModel(model);
        } catch (SQLException sQLException) {
               System.out.println("Eroro :" + sQLException.getMessage());
        }
        
    }
        

