    void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Category Id");
        model.addColumn("Category Name");
   
           try {
            //membuat objek user dan mengambil data dari database
            category cat = new category();
            ResultSet result = cat.tampilCategory();
            
            while (result.next()) {
            //tambahkan baris kedalam table model 
            model.addRow(new Object[]{
                result.getString("categoryId"),
                result.getString("categoryName")
           
            });
            }
            //set model ke Jtable
            tableCategory.setModel(model);
        } catch (SQLException sQLException) {
               System.out.println("Eroro :" + sQLException.getMessage());
        }
    }

