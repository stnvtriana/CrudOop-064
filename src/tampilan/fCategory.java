    private void tableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoryMouseClicked
        // TODO add your handling code here:
        int baris = tableCategory.rowAtPoint(evt.getPoint());
        
        int id = Integer.parseInt(tableCategory.getValueAt(baris, 0).toString());
        String nama = tableCategory.getValueAt(baris, 1).toString();
        
        tId.setText(String.valueOf(id));
        tNama.setText(nama);
        
       
    }//GEN-LAST:event_tableCategoryMouseClicked

